// Math serve para várias operações matemáticas

let n1 = 9.156465;


console.log(Math.floor(n1)); // Math.floor arrendonda para baixo
console.log(Math.ceil(n1)); // Math.ceil arrendonda para baixo
console.log(Math.round(n1)); // Math.round arrendoda para o lado que estiver puxando mais
console.log(Math.max(1,2,3,4,5,6,7,8,10)); // Math.max lhe da o maior valor dos números dados
console.log(Math.min(1,2,3,4,5,6,7,8,10)); // Math.min lhe da o maior valor dos números dados
console.log(Math.random()); // Math.random lhe da valores aleatórios
console.log(Math.pow(2, 10)); // Math.pow potencializa o valor
console.log(Math.sqrt(n1)); //Raiz quadrada

