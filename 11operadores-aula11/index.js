/**
 * Aritméticos
 * + = Adição & Concatenação
 * - = Subtração
 * ** = potenciação
 * % = Resto da divisão
 */


const n1 = 5;
const n2 = 10;
console.log(n1 + n2) // O resultado será 15
console.log(n1 - n2) // O resultado será -5
console.log(n1 ** n2) // O resultado será 9765625
console.log(n1 % n2) // O resultado será 5

// É possível utilizar vários valores

console.log(n1 + n2 * n1 % n2 ** n1) // O resultado será 55

